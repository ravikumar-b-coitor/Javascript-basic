let x=30.90;
string= x.toString();
console.log(string); //Output:

expo=x.toExponential(5)
console.log(expo)  //Output:3.09000e+1

fixed=x.toFixed(8)
console.log(fixed); //Output:30.90000000

precise=x.toPrecision(1)
console.log(precise);  //Output:3e+1

value=(90+5).valueOf()
console.log(value);  //Output:95






