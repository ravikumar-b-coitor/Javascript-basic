//node arithmetic.js
console.log("javascript arithmetic operator");
let x= 100;
let y=100;

//addition
var a=x+y;
console.log("Addition = "+a); //Output: 200

//subraction 
var b=x-y;
console.log("Subraction = "+b); //Output: 0

//multiplication
var c=x*y;
console.log("Multiplication = "+c); //Output: 10000

//division
var d=x/y;
console.log("Division = "+d); //Output: 200

//modulus
var e=x%y;
console.log("Modulus = "+e);  //Output: 0

//exponential
var h=x**2;
console.log("Exponential = "+h);  //Output: 10000

//increment
x++
var f=x;
console.log("Increment = "+f);  //Output: 101

//decrement
y--
var g=y;
console.log("Decrement = "+g); //Output: 99